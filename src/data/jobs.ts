// Helper to generate a relative date based on the current date
const getRotatingDate = (index: number) => {
  const now = new Date();
  // Spread jobs across the last 7 days
  // First 5 jobs are "Today", next 5 are "Yesterday", etc.
  const daysAgo = Math.floor(index / 5);
  const date = new Date(now);
  date.setDate(now.getDate() - daysAgo);
  return date.toISOString().split('T')[0];
};

